    public String Regex(String motif,String chaine){
        String res;
        Pattern p;
        Matcher m;
        p = Pattern.compile(motif+"\\d+");
        m = p.matcher(chaine);
        m.find();
        res = m.group();
        p = Pattern.compile("\\d+");
        m = p.matcher(res);
        m.find();
        res = m.group();
        return res;
    }
    
    
