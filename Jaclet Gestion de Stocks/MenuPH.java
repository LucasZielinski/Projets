    private void btnValiderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnValiderMouseClicked
        int choix = lstPharmacie.getSelectedIndex();
        if(choix != -1){
            String val = lstPharmacie.getModel().getElementAt(choix);
            int unId = Integer.parseInt(Regex("Demande : ",val));
            Demande uneDemande = Passerelle.donneUneDemande(unId);
            Passerelle.validerDemande(uneDemande);
            DefaultListModel listModel = new DefaultListModel();
            String libelle;
            for (Demande demande : Passerelle.donnerToutesLesDemandes()) {
            libelle=Passerelle.getMedocsDemandeParId(demande.getIdDemande());
            listModel.addElement(demande + libelle);
            libelle="";
            }
            lstPharmacie.setModel(listModel);
        }
        else{
            JOptionPane.showMessageDialog(null,"Veuillez s√©l√©ctionner un objet dans la liste (en cliquant dessus)");
        }
    }//GEN-LAST:event_btnValiderMouseClicked

