    public String Regex(String chaine){
        String res;
        Pattern p;
        Matcher m;
        p = Pattern.compile("Demande : \\d+");
        m = p.matcher(chaine);
        m.find();
        res = m.group();
        p = Pattern.compile("\\d+");
        m = p.matcher(res);
        m.find();
        res = m.group();
        return res;
    }
    
