    private void btnSupprimerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSupprimerMouseClicked
        String chaine = (String) lstDemandes.getSelectedValue();
        if(chaine != null){
            int idDemande = Integer.parseInt(Regex(chaine));
            Demande uneDemande = Passerelle.donneUneDemande(idDemande);
            Passerelle.supprimerDemande(uneDemande);
            DefaultListModel listModel = new DefaultListModel();
            String libelle;
            for (Demande demande : Passerelle.donnerLesDemandesParService(leService.getIdService())){
            libelle=Passerelle.getMedocsDemandeParId(demande.getIdDemande());
            listModel.addElement(demande + libelle);
            libelle="";
            }
            lstDemandes.setModel (listModel);
        }
        else{
            JOptionPane.showMessageDialog(null,"Veuillez séléctionner un objet dans la liste (en cliquant dessus)");
        }
    }//GEN-LAST:event_btnSupprimerMouseClicked

